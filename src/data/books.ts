export const getFeaturedBooks = () => books.filter(book => book.featured);
export const getBestsellers = () => books.filter(book => book.bestseller);
export const getBooksByGenre = (genre: string) => books.filter(book => book.genre === genre);
export const getBookById = (id: string) => books.find(book => book.id === id);
export const searchBooks = (query: string) => {
  const lowerQuery = query.toLowerCase();
  return books.filter(
    book =>
      book.title.toLowerCase().includes(lowerQuery) ||
      book.author.toLowerCase().includes(lowerQuery) ||
      book.genre.toLowerCase().includes(lowerQuery) ||
      book.description.toLowerCase().includes(lowerQuery)
  );
};
